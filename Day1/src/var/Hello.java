		System.out.println("나의 두번째 프로그램");
		// 여기에는 변수를 테스트 해보겠습니다
		
		// 저장후 컨트럴 + F11 (run)
		// 플러스 표시는 결합의 의미
		
		// 기본 데이터 Type 4 종류
		// 정수: byte, short(3만까지), int(+ - 21억), long(더큰거)
