package var;

public class Cal {

	public static void main(String[] args) {

		int num1 = 200;
		int num2 = 100;
		
		// 복사 -> 컨트럴 + 알트 + 화살표(밑으로)
		
		System.out.println(num1 + num2);
		System.out.println(num1 - num2);
		System.out.println(num1 * num2);
		System.out.println(num1 / num2);
		System.out.println(num1 % num2); // 나머지 -> 짝수 홀수 판단할때, 2로 나누어 나머지가 있으면 홀수, 없으면 짝수
		
		int num3 = num1 + num2;
		
		System.out.println(num3);
			
		
		
	}

}
